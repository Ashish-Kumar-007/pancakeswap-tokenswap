const interactWithContract = async () => {
  try {
    if (typeof window.ethereum === "undefined") {
      throw new Error("MetaMask is not installed or not accessible.");
    }

    await window.ethereum.request({ method: "eth_requestAccounts" }); // Request user permission to access accounts

    const web3Provider = new Web3(window.ethereum);
    const accounts = await web3Provider.eth.getAccounts();
    const tokenIn = "0xFa60D973F7642B748046464e165A65B7323b0DEE";
    const tokenOut = "0xae13d989daC2f0dEbFf460aC112a837C89BAa7cd";
    const amountIn = web3Provider.utils.toWei("1", "ether");
    const pancakeswap = new web3Provider.eth.Contract(ABI, routerAddress);

    try {
      const amounts = await pancakeswap.methods
        .getAmountsOut(amountIn, [tokenIn, tokenOut])
        .call();
      console.log(amounts.toString());
      const deadline = Math.floor(Date.now() / 1000) + 60 * 20;
      console.log(deadline);

      const amountOutMin = new web3Provider.utils.BN(amounts[1].toString());

      console.log(`
        Buying new token
        tokenIn: ${web3Provider.utils.fromWei(
          amountIn,
          "ether"
        )} ${tokenIn} (CAKE)
        tokenOut: ${amountOutMin}
        )} ${tokenOut} (USDT)
     `);

      const tx = await pancakeswap.methods
        .swapExactTokensForTokens(
          amountIn,
          amounts[1],
          [tokenIn, tokenOut],
          accounts[0],
          deadline
        )
        .send({ from: accounts[0] });
      console.log("tx status : ", tx);
    } catch (error) {
      console.error("Error executing the transaction:", error);
    }
    // Call a function on the contract
    // const result = await contract.methods.someFunction().call();
    // console.log(result);

    // Display the result on the webpage
    // result.textContent = `Result: ${result}`;
  } catch (error) {
    console.error("Error interacting with the contract:", error);
    // result.textContent = "Error interacting with the contract";
  }
};

